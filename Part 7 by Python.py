
def main():
    print("Вычисление площади поверхности параллелепипеда.")
    print("Введите исходные данные:")

    l = float(input("Длина (см) -> "))
    w = float(input("Ширина (см) -> "))
    h = float(input("Высота (см) -> "))
    
    s = 2 * (l * w + l * h + w * h)

    print(f"Площадь поверхности: {s} кв. см.")

if __name__ == '__main__':
    main()